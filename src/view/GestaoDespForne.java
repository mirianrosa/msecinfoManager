    private void botaoAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAtualizarActionPerformed
        // TODO add your handling code here:
        
        if (jFornecedors.getSelectedRow() != -1){
                   
            boolean statusRight;
        
            if (txtStatuspagamento.getSelectedItem().equals("Selecione...")){
                statusRight = false;
                JOptionPane.showMessageDialog(null, "Status de pagamento não foi definido!");
            } else {
                statusRight = true;
            }
        
            double multa;
            double juros;
            double preco = Double.parseDouble(txtPreco.getText());
            
            if (txtMulta.getText().equals("")){
                String mult = "0";
                multa = Double.parseDouble(mult);
            } else {
                multa = Double.parseDouble(txtMulta.getText());
            }
            
            if (txtJuros.getText().equals("")){
                String jurs = "0";
                juros = Double.parseDouble(jurs);
            } else {
                juros = Double.parseDouble(txtJuros.getText());
            }
            
            if (multa != 0) {
                preco = preco + multa;
            }
            
            if (juros != 0) {
                preco = preco + (preco * juros/100); //aplica juros em cima do valor + multa
            }
            
            if (statusRight){
                
                Fornecedor fornecedor = new Fornecedor();
                FornecedorDAO fornecedorDao = new FornecedorDAO();

                fornecedor.setStatuspagamento((String) txtStatuspagamento.getSelectedItem());
                fornecedor.setPreco(preco);
                fornecedor.setId((int)jFornecedors.getValueAt(jFornecedors.getSelectedRow(),0));
                fornecedorDao.updatePaga(fornecedor);
                readTable();

                //JOptionPane.showMessageDialog(null, "Fornecedor atualizado com sucesso.");

                txtNome.setText("");
                txtCpf.setText("");
                txtPreco.setText("");
                txtVencimento.setText("");
                txtMulta.setText("");
                txtJuros.setText("");
                txtStatuspagamento.setSelectedItem("Selecione...");
            }
            
        } else {
            JOptionPane.showMessageDialog(null, "Nenhum fornecedor foi selecionado!");
        }
    }//GEN-LAST:event_botaoAtualizarActionPerformed

