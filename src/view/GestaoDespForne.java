    private void readTable(){
        
        DefaultTableModel dtmFornecedors = (DefaultTableModel) jFornecedors.getModel();
        dtmFornecedors.setNumRows(0);
        FornecedorDAO fdao = new FornecedorDAO();
        
        for (Fornecedor f: fdao.read()){
            
            dtmFornecedors.addRow(new Object[]{  
                f.getId(),
                f.getEmpresa(),
                f.getCnpj(),
                f.getPreco(),
                f.getVencimento(),
                f.getStatuspagamento()
            });   
        }
    }

